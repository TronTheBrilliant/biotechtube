function parseMarketCap(str) {
  // "$811.51 B" -> 811510000000, "$99.13 M" -> 99130000, "$0 M" -> 0
  const match = str.match(/^\$([\d.]+)\s*([BM])?$/);
  if (!match) return 0;
  const num = parseFloat(match[1]);
  const unit = match[2];
  if (unit === 'B') return Math.round(num * 1e9);
  if (unit === 'M') return Math.round(num * 1e6);
  return Math.round(num);
}

